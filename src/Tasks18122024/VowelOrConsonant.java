package Tasks18122024;

public class VowelOrConsonant {
	
	public static void main(String []args) {
		
		String letter = "A";
		
		switch (letter) {
		
		case "a":
			System.out.println(letter + " is vowel");
			break;
		case "e":
			System.out.println(letter + " is vowel");
			break;
		case "i":
			System.out.println(letter + " is vowel");
			break;
		case "o":
			System.out.println(letter + " is vowel");
			break;
		case "u":
			System.out.println(letter + " is vowel");
			break;
			
		default: 
			System.out.println(letter + " is consonant");
		}
		
		
	}

}
